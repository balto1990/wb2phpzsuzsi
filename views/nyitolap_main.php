A magyarországi szélerőművek listája a 2011-ig üzembe helyezett szélerőműveket tartalmazza. Magyarországon összesen 37 szélerőmű van, összesen 172 toronnyal, 329 325 kW beépített teljesítménnyel. A legtöbb szélerőmű az ország északnyugati részén, főként Komárom és Mosonmagyaróvár környékén található, de van például a Tési-fennsíkon is, Csetény és Szápár térségében.

A magyarországi szélerőmű-létesítési engedélyekért tapasztalt nagy keresletet az magyarázza, hogy egy 2 megawattos szélturbina évente mintegy 100 millió forint bevételt hoz. Az ebből nyert áramot ugyanis a helyi szolgáltató vagy a Magyar Villamos Művek törvényben rögzített áron köteles átvenni, és ez az ár majdnem a duplája a hazai erőművek átlagárának.

<br><br>

A Kulcsi Szélerőmű az utóbbi években közkedvelté vált a tanulmányi kirándulások célállomásának. A WINFO Kft irodája mellett egy 50 fő befogadóképességű előadóterem található, ahol gyakran tartunk előadásokat a szélerőművek működéséről és az alternatív, megújuló energiaforrások szerepéről és környezetvédelmi fontosságáról.

